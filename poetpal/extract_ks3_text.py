def extract_pdf_text(kid, path):
    """Extract text before 一、作者簡介 (or 一、 背景資料 for second poem in KS3_11)"""
    reader = PdfReader(path)
    full_text = ''
    for page in reader.pages:
        full_text += page.extract_text() or ''
    
    # Try multiple marker variants (with/without space)
    markers = ['一、作者簡介', '一、 作者簡介', '一 、 作者簡介']
    
    if kid == 'KS3_11':
        # Special case: two poems. 燕詩 before 一、作者簡介, 慈烏夜啼 before 一、 背景資料
        part1_end = -1
        for m in markers:
            part1_end = full_text.find(m)
            if part1_end >= 0:
                break
        part1 = full_text[:part1_end].strip() if part1_end >= 0 else full_text
        
        # Find 慈烏夜啼 section (starts after 燕詩's 賞析)
        ciwu_start = full_text.find('慈烏夜啼')
        if ciwu_start >= 0:
            part2_start = ciwu_start
            part2_end = full_text.find('一、 背景資料', part2_start)
            if part2_end < 0:
                part2_end = full_text.find('一、背景資料', part2_start)
            part2 = full_text[part2_start:part2_end].strip() if part2_end >= 0 else full_text[part2_start:].strip()
            return part1 + '\n\n' + part2
        return part1
    
    idx = -1
    for marker in markers:
        idx = full_text.find(marker)
        if idx >= 0:
            break
    if idx >= 0:
        return full_text[:idx].strip()
    return full_text.strip()


def apply_annotations(text, annotations):
    """Replace annotated terms with {word|definition} format. Process longer phrases first.
    Use placeholders to avoid replacing terms inside definition strings."""
    if not annotations:
        return text
    
    blocks = []
    
    def protect_blocks(s):
        nonlocal blocks
        def save_block(m):
            blocks.append(m.group(0))
            return f'\x00B{len(blocks)-1}\x00'
        return re.sub(r'\{[^{}|]+\|[^{}]*\}', save_block, s)
    
    def restore_blocks(s):
        for i, block in enumerate(blocks):
            s = s.replace(f'\x00B{i}\x00', block)
        return s
    
    # Sort by key length descending so longer phrases get replaced first
    sorted_keys = sorted(annotations.keys(), key=len, reverse=True)
    
    for key in sorted_keys:
        defn = annotations[key]
        # Match: key + footnote number (avoids wrong replacements e.g. 若 in 乾若)
        pattern = re.escape(key) + r'\s*\d+\s*'
        replacement = '{' + key + '|' + defn + '}'
        text = re.sub(pattern, replacement, text)
        text = protect_blocks(text)  # Protect before next replacement
    
    text = restore_blocks(text)
    
    # Remove orphaned footnote numbers (standalone digits that were between words)
    text = re.sub(r'\s+\d+\s*', ' ', text)
    text = re.sub(r'\s+\d+$', '', text)
    text = re.sub(r'^\d+\s+', '', text)
    
    # Clean up extra spaces
    text = re.sub(r'  +', ' ', text)
    text = re.sub(r'\n +', '\n', text)
    text = re.sub(r' +\n', '\n', text)
    
    return text.strip()


def main():
    results = {}
    for kid, path in PDF_CONFIG:
        raw = extract_pdf_text(kid, path)
        ann = ANNOTATIONS.get(kid, {})
        processed = apply_annotations(raw, ann)
        results[kid] = processed
        print(f"=== {kid} ===\n{processed[:400]}...\n")
    
    return results


